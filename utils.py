def draw_box(data):

    # 将字典数据转换为DataFrame格式
    df = pd.DataFrame({'Sentence Length': list(data.keys()), 'Count': list(data.values())})

    # 绘制箱型图
    df.boxplot(column='Sentence Length')

    # 添加标题和轴标签
    plt.title('Sentence Length Distribution')
    plt.ylabel('Sentence Length')

    # 显示图形
    plt.show()


